import { Component, Injector, ViewChild, ViewEncapsulation, AfterViewInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { appModuleAnimation } from '@shared/animations/routerTransition';
import { AppComponentBase } from '@shared/common/app-component-base';
import { TaxDto, TaxServiceProxy } from '@shared/service-proxies/service-proxies';
import { LazyLoadEvent } from 'primeng/api';
import { Paginator } from 'primeng/paginator';
import { Table } from 'primeng/table';
import { CreateOrEditTaxModalComponent } from '../create-edit-tax/create-or-edit-tax-modal.component';
import { finalize } from 'rxjs/operators';

@Component({
    templateUrl: './taxes.component.html',
    encapsulation: ViewEncapsulation.None,
    styleUrls: ['./taxes.component.less'],
    animations: [appModuleAnimation()],
})
export class TaxesComponent extends AppComponentBase implements AfterViewInit {
    @ViewChild('createOrEditTaxModal', { static: true }) createOrEditTaxModal: CreateOrEditTaxModalComponent;
    @ViewChild('dataTable', { static: true }) dataTable: Table;
    @ViewChild('paginator', { static: true }) paginator: Paginator;
    
    //Filters
    filterText = '';

    constructor(
        injector: Injector,
        private _taxService : TaxServiceProxy,
        private _activatedRoute: ActivatedRoute
    ) {
        super(injector);
        this.filterText = this._activatedRoute.snapshot.queryParams['filterText'] || '';
    }

    ngAfterViewInit(): void {
        this.primengTableHelper.adjustScroll(this.dataTable);
    }

    getTaxes(event?: LazyLoadEvent) {
        if (this.primengTableHelper.shouldResetPaging(event)) {
            this.paginator.changePage(0);
            return;
        }

        this.primengTableHelper.showLoadingIndicator();
        this._taxService
            .getTaxes(
                    this.filterText,
                    this.primengTableHelper.getSorting(this.dataTable),
                    this.primengTableHelper.getMaxResultCount(this.paginator, event),
                    this.primengTableHelper.getSkipCount(this.paginator, event)
            )
            .pipe(finalize(() => this.primengTableHelper.hideLoadingIndicator()))
            .subscribe((result) => {
                this.primengTableHelper.totalRecordsCount = result.totalCount;
                this.primengTableHelper.records = result.items;
                this.primengTableHelper.hideLoadingIndicator();
            });
    }

    reloadPage(): void {
        this.paginator.changePage(this.paginator.getPage());
    }

    createTax(): void {
        this.createOrEditTaxModal.show();
    }

    deleteTax(taxItem: TaxDto): void {
        this.message.confirm(this.l('TaxDeleteWarningMessage', taxItem.name), this.l('AreYouSure'), (isConfirmed) => {
            if (isConfirmed) {
                this._taxService.deleteTax(taxItem.id).subscribe(() => {
                    this.reloadPage();
                    this.notify.success(this.l('SuccessfullyDeleted'));
                });
            }
        });
    }
}
